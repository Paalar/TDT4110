print ("Dette er en for-løkke")
for n in range(5):
    print (n+1, end=" ")
print("\n")

print ("Dette er en while-løkke")
x = 1
while x < 6:
    print(x, end=" ",)
    x = x + 1
print("\n")

print ("Dette er også en for-løkke")
for o in range(15, 0,-1):
    print(o, end=" ")
print("\n")


z = 15
